#include <iostream>
#include <cmath>

using namespace std;

int main() {
    double num;
    cin >> num;

    cout << abs(num) << '\n';
    cout << sin(num) << '\n';
    cout << cos(num) << '\n';
    cout << tan(num) << '\n';
    cout << log(num) << '\n';
    cout << log10(num) << '\n';
    cout << sqrt(num) << '\n';
    cout << exp(num) << '\n';

    return 0;
}

